def e3():
    # Leer los dos lados del rectángulo
    lado1 = int(input("Ingrese el primer lado del rectángulo: "))
    lado2 = int(input("Ingrese el segundo lado del rectángulo: "))

    # Calcular la superficie del rectángulo
    superficie = lado1 * lado2

